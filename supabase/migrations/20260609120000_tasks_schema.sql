CREATE OR REPLACE FUNCTION public.moddatetime()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS handle_updated_at ON public.tasks;
CREATE TRIGGER handle_updated_at
  BEFORE UPDATE ON public.tasks
  FOR EACH ROW EXECUTE FUNCTION public.moddatetime();

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
BEGIN
  INSERT INTO public.profiles (id, full_name, email, avatar_url, role, created_at, last_sign_in_at)
  VALUES (
    NEW.id,
    NEW.raw_user_meta_data ->> 'full_name',
    NEW.email,
    NEW.raw_user_meta_data ->> 'avatar_url',
    'employee',
    NEW.created_at,
    NEW.last_sign_in_at
  )
  ON CONFLICT (id) DO NOTHING;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

CREATE OR REPLACE FUNCTION public.handle_user_update()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
BEGIN
  UPDATE public.profiles
  SET last_sign_in_at = NEW.last_sign_in_at,
      email = NEW.email
  WHERE id = NEW.id;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_updated ON auth.users;
CREATE TRIGGER on_auth_user_updated
  AFTER UPDATE ON auth.users
  FOR EACH ROW
  WHEN (OLD.last_sign_in_at IS DISTINCT FROM NEW.last_sign_in_at OR OLD.email IS DISTINCT FROM NEW.email)
  EXECUTE FUNCTION public.handle_user_update();

CREATE OR REPLACE FUNCTION public.get_user_role(p_user_id uuid)
RETURNS text
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(role, 'employee') FROM public.profiles WHERE id = p_user_id;
$$;

CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT public.get_user_role(auth.uid()) = 'admin';
$$;

CREATE OR REPLACE FUNCTION public.is_manager()
RETURNS boolean
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT public.get_user_role(auth.uid()) IN ('admin', 'manager');
$$;

CREATE OR REPLACE FUNCTION public.task_user_has_access(p_task_id bigint, p_user_id uuid)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_task public.tasks%ROWTYPE;
BEGIN
  SELECT * INTO v_task FROM public.tasks WHERE id = p_task_id;
  IF NOT FOUND THEN RETURN FALSE; END IF;
  IF v_task.user_id = p_user_id OR v_task.created_by = p_user_id THEN RETURN TRUE; END IF;
  IF EXISTS (
    SELECT 1 FROM jsonb_array_elements(COALESCE(v_task.assignees, '[]'::jsonb)) elem
    WHERE elem ->> 'user_id' = p_user_id::text
  ) THEN RETURN TRUE; END IF;
  IF v_task.project_id IS NOT NULL AND public.is_project_member(v_task.project_id, p_user_id) THEN
    RETURN TRUE;
  END IF;
  RETURN public.is_admin();
END;
$$;

CREATE OR REPLACE FUNCTION public.can_access_task_from_attachment_path(p_file_path text)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_task_id bigint;
BEGIN
  BEGIN
    v_task_id := (string_to_array(p_file_path, '/'))[2]::bigint;
  EXCEPTION WHEN OTHERS THEN
    RETURN FALSE;
  END;
  IF v_task_id IS NULL THEN RETURN FALSE; END IF;
  RETURN public.task_user_has_access(v_task_id, auth.uid());
END;
$$;

CREATE OR REPLACE FUNCTION public.is_project_member(p_project_id bigint, p_user_id uuid)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.project_members
    WHERE project_id = p_project_id AND user_id = p_user_id
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.create_new_task_assignment_notification()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  assigner_name text;
BEGIN
  SELECT full_name INTO assigner_name FROM public.profiles WHERE id = auth.uid();
  IF NEW.user_id IS NOT NULL AND NEW.user_id <> auth.uid() THEN
    INSERT INTO public.notifications (user_id, actor_id, type, data)
    VALUES (
      NEW.user_id, auth.uid(), 'new_task_assigned',
      jsonb_build_object('task_id', NEW.id, 'task_title', NEW.title, 'assigner_name', assigner_name)
    );
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_new_task_created_for_other ON public.tasks;
CREATE TRIGGER on_new_task_created_for_other
  AFTER INSERT ON public.tasks
  FOR EACH ROW EXECUTE FUNCTION public.create_new_task_assignment_notification();

DROP TRIGGER IF EXISTS on_task_reassigned ON public.tasks;
CREATE TRIGGER on_task_reassigned
  AFTER UPDATE OF user_id ON public.tasks
  FOR EACH ROW
  WHEN (OLD.user_id IS DISTINCT FROM NEW.user_id)
  EXECUTE FUNCTION public.create_new_task_assignment_notification();

CREATE OR REPLACE FUNCTION public.create_new_comment_notification()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  task_info RECORD;
  commenter_name text;
BEGIN
  SELECT user_id, created_by, title INTO task_info FROM public.tasks WHERE id = NEW.task_id;
  SELECT full_name INTO commenter_name FROM public.profiles WHERE id = NEW.user_id;

  IF task_info.user_id IS NOT NULL AND task_info.user_id <> NEW.user_id THEN
    INSERT INTO public.notifications (user_id, actor_id, type, data)
    VALUES (task_info.user_id, NEW.user_id, 'new_comment',
            jsonb_build_object('task_id', NEW.task_id, 'task_title', task_info.title, 'commenter_name', commenter_name));
  END IF;

  IF task_info.created_by IS NOT NULL
     AND task_info.created_by <> NEW.user_id
     AND task_info.created_by <> task_info.user_id THEN
    INSERT INTO public.notifications (user_id, actor_id, type, data)
    VALUES (task_info.created_by, NEW.user_id, 'new_comment',
            jsonb_build_object('task_id', NEW.task_id, 'task_title', task_info.title, 'commenter_name', commenter_name));
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_new_comment ON public.task_comments;
CREATE TRIGGER on_new_comment
  AFTER INSERT ON public.task_comments
  FOR EACH ROW EXECUTE FUNCTION public.create_new_comment_notification();

