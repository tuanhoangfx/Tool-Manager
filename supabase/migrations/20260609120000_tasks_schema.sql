INSERT INTO storage.buckets (id, name, public)
VALUES ('task-attachments', 'task-attachments', true)
ON CONFLICT (id) DO NOTHING;

DROP POLICY IF EXISTS "Users can view task attachments." ON storage.objects;
CREATE POLICY "Users can view task attachments." ON storage.objects
  FOR SELECT USING (bucket_id = 'task-attachments' AND public.can_access_task_from_attachment_path(name));

DROP POLICY IF EXISTS "Users can upload task attachments." ON storage.objects;
CREATE POLICY "Users can upload task attachments." ON storage.objects
  FOR INSERT WITH CHECK (
    bucket_id = 'task-attachments'
    AND auth.uid()::text = (storage.foldername(name))[1]
    AND public.can_access_task_from_attachment_path(name)
  );

DROP POLICY IF EXISTS "Users can delete their own task attachments." ON storage.objects;
CREATE POLICY "Users can delete their own task attachments." ON storage.objects
  FOR DELETE USING (
    bucket_id = 'task-attachments'
    AND auth.uid()::text = (storage.foldername(name))[1]
    AND public.can_access_task_from_attachment_path(name)
  );

DROP POLICY IF EXISTS "Admins can manage all task attachment objects." ON storage.objects;
CREATE POLICY "Admins can manage all task attachment objects." ON storage.objects
  FOR ALL USING (bucket_id = 'task-attachments' AND public.is_admin())
  WITH CHECK (bucket_id = 'task-attachments' AND public.is_admin());

